double chi_squared(int length, map<char,int> monograms, map<string,int> digrams){
  double sum = 0;
  for(auto m : expected_m){
    ei = length*m.second;
    if(monograms.find(m.first)==-1){
      chi_squared = ei;
    }
    else{
      chi_squared = pow(monograms[m.first]-ei,2)/(ei);
    }
    sum+=chi_squared*1.0;
  }
  for(auto d : expected_d){
    ei = length*d.second;
    if(digrams.find(d.first)==-1){
      chi_squared = ei;
    }
    else{
      chi_squared = pow(digrams[d.first]-ei,2)/(ei);
    }
    sum+=chi_squared*1.0;
  }
  return sum;
}
